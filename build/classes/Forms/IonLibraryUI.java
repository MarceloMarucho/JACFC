    private void btnSaveIonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveIonActionPerformed

        try {
            File statText = new File(fileName);
            FileOutputStream is = new FileOutputStream(statText);
            OutputStreamWriter osw = new OutputStreamWriter(is);
            Writer w = new BufferedWriter(osw);
            try {
                // Start writing input file.
                for (int i = 0; i < tblTable.getRowCount(); i++){
                    w.write(tblTable.getValueAt(i, 0) + " "
                        + tblTable.getValueAt(i, 1) + " "
                        + (String) tblTable.getValueAt(i, 2) + " "
                        + (String) tblTable.getValueAt(i, 3) + " "
                        + (String) tblTable.getValueAt(i, 4) + " "
                        + (String) tblTable.getValueAt(i, 5) + " "
                        + (String) tblTable.getValueAt(i, 6) + " "
                        + (String) tblTable.getValueAt(i, 7) + " "
                        + (String) tblTable.getValueAt(i, 8) + " "
                        + (String) tblTable.getValueAt(i, 9) + " "
                        + (String) tblTable.getValueAt(i, 10) + " "
                        + (String) tblTable.getValueAt(i, 11) + " "
                        + (String) tblTable.getValueAt(i, 12) + " " + "\n");
                }
                w.close();
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(null, ShowMessages.getLibrarySuccess(),
                    "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                Toolkit.getDefaultToolkit().beep();
                JOptionPane.showMessageDialog(null, ShowMessages.getSavingFile(),
                    null, JOptionPane.WARNING_MESSAGE);
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ShowMessages.getNotFile(),
                null, JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveIonActionPerformed

