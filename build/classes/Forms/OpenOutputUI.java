    private void chkDisplayDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkDisplayDataActionPerformed

        boolean retval;
        if(chkDisplayData.isSelected()){
            retval = Utilities.getAnalysisPath().contains("DensityProfile");
            if(retval){
                CreateTables.set_table_item_list(Results.strDensityProfile);
            }
            retval = Utilities.getAnalysisPath().contains("ElectrostaticPotential");
            if(retval){
                CreateTables.set_table_item_list(Results.strElectrostaticPotential);
            }
            retval = Utilities.getAnalysisPath().contains("IntegratedCharge");
            if(retval){
                CreateTables.set_table_item_list(Results.strIntegratedCharge);
            }
            retval = Utilities.getAnalysisPath().contains("IonContributions");
            if(retval){
                CreateTables.set_table_item_list(Results.strIonContributions);
            }
            retval = Utilities.getAnalysisPath().contains("Pressure");
            if(retval){
                CreateTables.set_table_item_list(Results.strPressure);
            }
            //////            retval = Utilities.getAnalysisPath().contains("pH");
            //////            if(retval){
                //////                CreateTables.set_table_item_list(Results.strPH);
                //////            }
        }else{
            CreateTables.dispose_data();
        }
    }//GEN-LAST:event_chkDisplayDataActionPerformed

